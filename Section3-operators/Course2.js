//Arthimetic operators 
let x = 10;
let y = 3;
console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y); //division
console.log(x % y); //reminder of the division
console.log(x ** y) //exponent indicate by two star 

//increment operator (++)
console.log(++x); //output 11
console.log(x++); //output 10 
console.log(x); //print output 11

//decrement operator(--)
console.log(--x); //output 9 apply operator on first and display x value
console.log(x--); //output 9