//Comparsion Operators 
// result of comparsion operators will be bollean (either true of false it will return the output)
let x = 1 ;

//Rational Operators 
console.log(x > 0);  //true
console.log(x >= 1); //true
console.log(x < 1);  //false
console.log(x <= 1); //false

//Equality operator
console.log(x == 1);
console.log(x == '1');
console.log(x === 1);
console.log(x === '1');

//!= is similar to two equal to compare only value
console.log(x != 1); //false
console.log(x != '1'); //false

//!== is similar to three equal to compare both data type and value
console.log( x !== 1); //false
console.log(x !== '1'); //true